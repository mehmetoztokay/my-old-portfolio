if (pItemsData) {
    for (let i = 0; i < pItemsData.length; i++) {
        var portfolioItemsCont = document.querySelector('.portfolio-items-container');
        var portfolioItem = document.createElement('div');
        portfolioItem.setAttribute('class', 'portfolio-item');
        portfolioItem.setAttribute('data-class', 'web');
        portfolioItem.setAttribute('data-id', pItemsData[i].dataId);
        portfolioItem.style.backgroundColor = pItemsData[i].imgItemBack;
        var portfolioItemImg = document.createElement('img');
        portfolioItemImg.setAttribute('class', 'portfolio-item-img');
        portfolioItemImg.setAttribute('src', pItemsData[i].imgItem);
        var portfolioItemInfo = document.createElement('div');
        portfolioItemInfo.setAttribute('class', 'portfolio-item-info');
        portfolioItemInfo.textContent = pItemsData[i].imgItemInfo;
        portfolioItem.appendChild(portfolioItemImg);
        portfolioItem.appendChild(portfolioItemInfo);
        portfolioItemsCont.appendChild(portfolioItem);
    }
}



